
# diferenets tipos de cadena 

cadena = "Universidad Tecnologica de Leon"
print(cadena)
print(cadena.lower())
print(cadena.upper()) #este sirve para que todas als palabras sean en mayuscula
print(cadena.title())
print(cadena.find("de")) #juntar la aparicion de una variable (buscar)
print(cadena.count("a")) #contar cuantas apariciones existen en ese caracter

textoFinal=cadena.replace("a","4") #remplazar cosas por ejemplo la letra a la cambias por el 4
print(textoFinal)
cadenaNueva=cadena.split(" ") # Te hace la separaciones en palabras de una cadena cada elemento de la lista es una palabra
print(cadenaNueva)



